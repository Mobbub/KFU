def vector_product(A, B):
    # Распакуем координаты векторов A и B
    a1, a2, a3 = A  # координаты вектора A
    b1, b2, b3 = B  # координаты вектора B

    # Вычисление компонент векторного произведения
    c1 = a2 * b3 - a3 * b2  # первая компонент (x)
    c2 = a3 * b1 - a1 * b3  # вторая компонент (y)
    c3 = a1 * b2 - a2 * b1  # третья компонент (z)

    # Возвращаем результат в виде кортежа с компонентами вектора
    return (c1, c2, c3)

# Определяем векторы A и B
A = (1, 2, 3)  # вектор A
B = (4, 5, 6)  # вектор B

# Вычисляем векторное произведение векторов A и B
C = vector_product(A, B)

# Печатаем результат
print(C)  # выводит (чему равно векторное произведение)